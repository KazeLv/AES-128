#include "aes128.h"

using namespace std;

int main()
{
	byte_t plainText[4][4] = {0x00, 0x44, 0x88, 0xcc,
							  0x11, 0x55, 0x99, 0xdd,
							  0x22, 0x66, 0xaa, 0xee,
							  0x33, 0x77, 0xbb, 0xff};

	byte_t key[4][4] = {0x00, 0x04, 0x08, 0x0c,
						0x01, 0x05, 0x09, 0x0d,
						0x02, 0x06, 0x0a, 0x0e,
						0x03, 0x07, 0x0b, 0x0f};

	cout << "PLAINTEXT:\t\t";
	printBlock(plainText);
	cout << "KEY:\t\t\t";
	printBlock(key);
	cout << endl;

	aes_128(plainText, key, true);
	cout << endl;
	aes_128(plainText, key, false);

	return 0;
}